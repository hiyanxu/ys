$(function () {
    $("#firstPage").click(function () {      
        $("#mainDiv").animate({
            left: "200px"
        },
                600,
                function () {
                    $("#mainDiv").css("zIndex", 0),
                    $("#centerOverView").css("zIndex", 0),
                    $("#workShop").css("zIndex", 0),
                    $("#project").css("zIndex", 0),
                    $("#course").css("zIndex", 0),
                    $("#achieve").css("zIndex", 0),
                    $("#scienceCulture").css("zIndex", 0),
                    $("#stuUnion").css("zIndex", 0),
                    $("#downLoad").css("zIndex", 0),              
                    $("this").css("zIndex", 1),
                     $("#mainDiv").css("zIndex", 2)
                }).animate({
                    left: "0px"
                },
                600
                );
    });
});
